package com.syntax.review3;

import java.util.Scanner;

public class ForLoops {
    public static void main(String[] args) {

        for (int i = 1; i <=5 ; i++) {
            System.out.println("Hello "+i);
        }
        System.out.println("**********************************");

        for (int a = 0; a < 8; a+=4) {
            System.out.println("Bye");
            
        }
        System.out.println("************************************");

        for (int b = 2; b > 10; b++) {
            System.out.println(b);
        }
        System.out.println("************************************");

        for (int j = 3; j <10 ; j--) {
            System.out.println(j);

        }
     }}


